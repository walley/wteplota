package org.walley.wteplota;
/******************************************************************************/
/******************************************************************************/

/******************************************************************************/
public class MessageEvent
        /******************************************************************************/
        /******************************************************************************/
        /******************************************************************************/
{
  public final String message;
  public int param_i;

  /******************************************************************************/
  public MessageEvent(String message)
  /******************************************************************************/
  {
    this.message = message;
  }

  /******************************************************************************/
  public MessageEvent(String message, int i)
  /******************************************************************************/
  {
    this.message = message;
    param_i = i;
  }
}
