package org.walley.wteplota;

/******************************************************************************/
public class message_event
/******************************************************************************/
{
  public final String message;
  public int param_i;

  /******************************************************************************/
  public message_event(String message)
  /******************************************************************************/
  {
    this.message = message;
  }

  /******************************************************************************/
  public message_event(String message, int i)
  /******************************************************************************/
  {
    this.message = message;
    param_i = i;
  }
}
